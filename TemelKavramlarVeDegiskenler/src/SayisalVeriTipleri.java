
public class SayisalVeriTipleri {

	public static void main(String[] args) {
		
		byte abc = 100 ;
		
		System.out.println("Byte değeri: " +abc);
		System.out.println("abc");
		// Çift tırnak içinde yazmamız bize ekrana yazdırılan veriyi farklı hale getirir.
		
		short shortDegiskeni = 1200;
		
		System.out.println("Short değeri: " + shortDegiskeni);
		
		int intDegeri = 120035;
		
		System.out.println("İnt Değeri: " + intDegeri);
		
		long longDegeri = 12345687;
		
		System.out.println("Long değeri: " + longDegeri);
		
	}

}